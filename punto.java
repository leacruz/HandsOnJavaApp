/*
*  Creacion de la clase punto, sirve para que sea mas presentable y con mas control de los puntos que estan en el diagrama de dispercion
*/
public class punto {
    float A;
    float B; 

    public punto() {
        A = 0;
        B = 0;
    }

    public punto(float PointA, float PointB){
        A = PointA;
        B = PointB;
    }
}